import com.liferay.portal.test.rule.Inject;
import com.liferay.portal.test.rule.LiferayIntegrationTestRule;
import com.liferay.portal.test.rule.PermissionCheckerMethodTestRule;

import java.util.Collections;

import org.junit.Before;
import org.junit.ClassRule;
import org.junit.Rule;
import org.junit.runner.RunWith;

/**
 * @author Cheryl Tang
 */
@RunWith(Arquillian.class)
public class FriendlyURLEntryLocalizationTableReferenceDefinitionTest
	extends BaseTableReferenceDefinitionTestCase {

	@ClassRule
	@Rule
	public static final AggregateTestRule aggregateTestRule =
		new AggregateTestRule(
			new LiferayIntegrationTestRule(),
			PermissionCheckerMethodTestRule.INSTANCE);

	@Before
	@Override
	public void setUp() throws Exception {
		super.setUp();

		_friendlyURLEntry = _friendlyURLEntryLocalService.addFriendlyURLEntry(
			group.getGroupId(),
			ClassNameLocalServiceUtil.getClassNameId(User.class),
			TestPropsValues.getUserId(),
			Collections.singletonMap("en", "url-title-en"),
			ServiceContextTestUtil.getServiceContext(group.getGroupId()));
	}

	@Override
	protected CTModel<?> addCTModel() throws Exception {
		return _friendlyURLEntryLocalService.updateFriendlyURLEntryLocalization(
			_friendlyURLEntry, "zh_CN", "url-title-zh");
	}

	@Inject
	private static FriendlyURLEntryLocalService _friendlyURLEntryLocalService;

	private FriendlyURLEntry _friendlyURLEntry;

}